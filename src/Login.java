    private boolean verifyUser()
    {
        boolean flag=false;
        String username,password;
        username=jTextField1.getText();
        password=jTextField2.getText();
        try
        {
        rs=stmt.executeQuery("select * from cryptography where user_id="+username);
        if(rs.next())
        {
            do
            {
                if(password.equals(rs.getString(2)))
                {
                    flag=true;
                    ID=username;
                }
                else
                {
                    JOptionPane.showMessageDialog(this,"Password Does Not Match. Please Try Again.");
                    jTextField2.setText(null);
                    break;
                }
            }while(rs.next());
        }
        
        }
        catch(Exception x)
        {
            JOptionPane.showMessageDialog(this,"Invalid User Id. Please try Again.");
            jTextField1.setText(null);
            jTextField2.setText(null);
        }
        return flag;
    }
